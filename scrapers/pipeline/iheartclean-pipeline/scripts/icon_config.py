# =============================================================================
# HELPER FUNCTIONS
# =============================================================================

def get_icon_by_slug(category: str, slug: str) -> dict:
    """Get icon data by category and slug."""
    icons = ALL_ICONS.get(category, {})
    for pos, data in icons.items():
        if data["slug"] == slug:
            return {"position": pos, **data}
    return None


def get_icon_by_position(category: str, position: int) -> dict:
    """Get icon data by category and grid position."""
    icons = ALL_ICONS.get(category, {})
    return icons.get(position)


def get_all_slugs_for_category(category: str) -> list:
    """Get all valid slugs for a category."""
    icons = ALL_ICONS.get(category, {})
    return [data["slug"] for data in icons.values()]


def get_valid_slugs() -> dict:
    """Get all valid slugs organized by category."""
    return {cat: get_all_slugs_for_category(cat) for cat in ALL_ICONS.keys()}


